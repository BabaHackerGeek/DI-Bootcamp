// Instructions:
// 1. Define and export two functions: `add` and `multiply`.
//    - `add(a, b)`: Returns the sum of `a` and `b`.
//    - `multiply(a, b)`: Returns the product of `a` and `b`.

function add(a, b) {
    return a + b;
}

function multiply(a, b) {
    return a * b;
}

module.exports = { add, multiply };
